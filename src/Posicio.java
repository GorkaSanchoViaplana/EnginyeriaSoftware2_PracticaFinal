public class Posicio {
    private float PosX;
    private float PosY;
    private float PosZ;

    public Posicio(float PosX, float PosY,float PosZ){
        this.PosX = PosX;
        this.PosY = PosY;
        this.PosZ = PosZ;
    }
    public float getPosX() {
        return PosX;
    }
    public void setPosX(float PosX) {
        this.PosX = PosX;
    }
    public float getPosY() {
        return PosY;
    }
    public void setPosY(float PosY) {
        this.PosY = PosY;
    }
    public float getPosZ() {
        return PosZ;
    }
    public void setPosZ(float PosZ) {
        this.PosZ = PosZ;
    }
}
